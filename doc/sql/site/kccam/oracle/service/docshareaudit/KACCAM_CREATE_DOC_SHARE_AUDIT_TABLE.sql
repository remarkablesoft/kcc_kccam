CREATE TABLE TS02_AM_DOC_SHARE_AUDIT
(
    OID		        VARCHAR2(11 BYTE)		NOT NULL,
    TARGET_OID_LIST	VARCHAR2(4000 BYTE)		NULL,
    EMAIL		    VARCHAR2(50 BYTE)		NULL,
    INPUT_DATE 	    DATE DEFAULT SYSDATE    NOT NULL,
    SHARE_TYPE	    VARCHAR2(8)		        NULL,

    ACCESS_URL      VARCHAR2(1000)          NULL,
    USER_IP         VARCHAR2(20)            NULL
);

