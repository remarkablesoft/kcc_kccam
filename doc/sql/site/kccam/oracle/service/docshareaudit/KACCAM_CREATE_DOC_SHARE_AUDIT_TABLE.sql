COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.OID IS '문서 공유/다운 로그 테이블 OID';
COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.TARGET_OID_LIST IS 'TARGET OID 리스트';
COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.EMAIL IS '공유/다운 이용자 EMAIL';
COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.INPUT_DATE IS '등록일시';
COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.SHARE_TYPE IS '문서공유/다운 타입';

COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.ACCESS_URL IS '접근 URL';
COMMENT ON COLUMN TS02_AM_DOC_SHARE_AUDIT.USER_IP IS '사용자 IP';